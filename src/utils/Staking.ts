export const stakeTokens = async (provider: ethers.BrowserProvider, stakingAddress: string, tokenAmount: string): Promise<void> => {
    try {
        const signer = await provider.getSigner(); 
        const contractWithSigner = new ethers.Contract(stakingAddress, Staking_ABI, signer);
        const amountInUnits = ethers.parseUnits(tokenAmount, 18);
        const tx = await contractWithSigner.stake(amountInUnits);
        await tx.wait();
        console.log('Staking:', tokenAmount);
    } catch (error) {
        console.error('Error staking tokens:', error);
        throw error; 
    }
};

export const unstakeTokens = async (provider: ethers.BrowserProvider, stakingAddress: string, tokenAmount: string): Promise<void> => {
    try {
        const signer = await provider.getSigner(); 
        const contractWithSigner = new ethers.Contract(stakingAddress, Staking_ABI, signer);
        const amountInUnits = ethers.parseUnits(tokenAmount, 18);
        const tx = await contractWithSigner.withdraw(amountInUnits);
        await tx.wait();
        console.log('Withdrawing:', tokenAmount);
    } catch (error) {
        console.error('Error withdrawing tokens:', error);
        throw error; 
    }
};